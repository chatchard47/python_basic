#เก็บค่าลงในตัวแปร์ 1 
str = "Basic Python programing."

print (str)
print (str,'My name is ') # การเชื่อมข้อความด้วยเครื่องหมาย , 
print (str[0])   #แสดงการเข้าถึงช่วงใน string ตัวแรก
print (str[-1])   #แสดงการเข้าถึงช่วงใน string ตัวสุดท้าย
print (str[0:5]) #แสดงการเข้าถึงช่วงใน string ตัว 0-5

#กำหนดเงื่อนไข in , not in
print ("Python"in str) #เช็กข้อความมีใน str หรือไม่
print ("555"not in str) #เช็กข้อความไม่มีใน str หรือไม่

