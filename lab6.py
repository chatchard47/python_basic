# class math 
# กลุ่มคำลั่ง คณิตศาสตร์
import math         #import function math มาฃใช้
num = math.pow(2,3) #คำสั่งยกกำลัง 2^3 จะได้=8
print (num)

num1 = math.sqrt(25) #คำสั่งถอดราก 25 ได้เท่ากับ 5
print(num1)

num2 = math.ceil(3.1) #คำสั่งปัดเศษขึ้น 3.1 เป็นเท่ากับ 4
print (num2)

num3 = math.floor(3.09) #คำสั่งปัดเศษขึ้น 3.09 เป็นเท่ากับ 3
print(num3)

num4 = math.pi #แสดงค่า พาย
print(num4)

#EX.1 ทดสอบการคิดเลข
print (num*num1)
print (math.ceil(num4*math.sqrt(16)))